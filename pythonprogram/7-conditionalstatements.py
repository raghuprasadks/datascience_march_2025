# if elif else

# if statement

a=33
if a>20:
    print("a is greater than 20")

# elif

a=33
b=33
if a>b:
    print("a is greater than b")
elif a==b:
    print("a is equal to b")

# else

a=33
b=34
if a>b:
    print("a is greater than b")
elif a==b:
    print("a is equal to b")
else:
    print("a is smaller than b")


